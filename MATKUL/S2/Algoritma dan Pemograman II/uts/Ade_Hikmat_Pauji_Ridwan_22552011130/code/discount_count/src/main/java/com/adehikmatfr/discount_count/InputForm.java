    private String generateID(String sparator) {
        // Menggunakan timestamp sebagai basis ID
        String timestamp = String.valueOf(System.currentTimeMillis());
        // Mengambil 3 karakter terakhir dari timestamp
        String id = timestamp.substring(timestamp.length() - 3);
        // Menambahkan prefix "ID-" ke depan ID
        id = sparator + "-" + id;
        // Mengembalikan hasil ID
        return id;
    }

