    private void saveProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveProductMouseClicked
        // TODO add your handling code here:
        double priceProductDouble;
        try {
            priceProductDouble = Double.parseDouble(this.priceProduct.getText());
        } catch (NumberFormatException e) {
            // Handling jika terjadi kesalahan saat mengubah teks menjadi double
            JOptionPane.showMessageDialog(null, "harga harus angka!");
            return;
        }

        Barang barang = new Barang(this.generateID("B"), this.nameProduct.getText(), priceProductDouble);
        if (barang.validasiBarang()) {
            JOptionPane.showMessageDialog(null, "Periksa kembali inputan anda!");
            return;
        }

        this.barangDataList.add(barang);

        // add to table
        this.defaultTableModelBarang.addRow(new Object[]{
            barang.getKodeBarang(),
            barang.getNama(),
            barang.getHarga()
        });

        this.defaultListModel.addElement(barang.getKodeBarang() + " - " + barang.getNama());
    }//GEN-LAST:event_saveProductMouseClicked

    private void saveMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMemberMouseClicked
        // TODO add your handling code here:
        Member member = new Member(this.generateID("M"), this.nameMember.getText(), this.phone.getText());
        if (member.validasiMember()) {
            JOptionPane.showMessageDialog(null, "Periksa kembali inputan anda!");
            return;
        }

        this.memberDataList.add(member);

        // add to table
        this.defaultTableModelMember.addRow(new Object[]{
            member.getKodeMember(),
            member.getNama(),
            member.getNomorTelepon()
        });

        this.memberList.addItem(member.getKodeMember() + " - " + member.getNama());
    }//GEN-LAST:event_saveMemberMouseClicked

    private void saveTrxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveTrxMouseClicked
        // TODO add your handling code here:
        var selectedItems = this.productList.getSelectedValuesList();
        ArrayList<Barang> barangSelected = new ArrayList<>();

        selectedItems.forEach((var item) -> {
            var split = item.split(" - ");
            if (split.length > 1) {
                for (Barang barang : this.barangDataList) {
                    if (barang.getKodeBarang().equals(split[0])) {
                        barangSelected.add(barang);
                        break;
                    }
                }
            }
        });

        var member = this.memberList.getSelectedItem().toString();
        var split = member.split(" - ");

        if (split.length > 1) {
            for (Member dataMember : this.memberDataList) {
                if (dataMember.getNama().equals(split[0])) {
                    member = dataMember.getKodeMember();
                    break;
                }
            }
        } else {
            member = "";
        }

        TransaksiPesanan newTransaksiPesanan = new TransaksiPesanan(barangSelected, member);

        String presentasiDiskonStr = Double.toString(newTransaksiPesanan.getPresentasiDiskon());
        this.jLabel12.setText(presentasiDiskonStr.replaceAll("\\.0$", ""));

        String getJumlahTotalStr = Double.toString(newTransaksiPesanan.getJumlahTotal());
        this.jLabel13.setText(getJumlahTotalStr.replaceAll("\\.0$", ""));

        String diskonStr = Double.toString(newTransaksiPesanan.getDiskon());
        this.jLabel14.setText(diskonStr.replaceAll("\\.0$", ""));

        String jumlahHargaStr = Double.toString(newTransaksiPesanan.getJumlahHarga());
        this.jLabel15.setText(jumlahHargaStr.replaceAll("\\.0$", ""));
    }//GEN-LAST:event_saveTrxMouseClicked

